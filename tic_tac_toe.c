const unsigned char cursor_tile[] = { 0xFF,0xFF,0xFF,0xFF,0xC3,0xC3,0xC3,0xC3,0xC3,0xC3,0xC3,0xC3,0xFF,0xFF,0xFF,0xFF };

// --- VARIÁVEIS GLOBAIS ---
unsigned char board[3][3], cur_x, cur_y, turn, winner, moves, g_i, g_j, g_input, g_last, g_starter = 1;
const unsigned char t_x[] = {6, 9, 12}, t_y[] = {6, 9, 12};
const unsigned char p_x[] = {48, 72, 96}, p_y[] = {48, 72, 96}; 

// --- SOM ---
void init_sound() { NR52_REG = 0x80; NR50_REG = 0x77; NR51_REG = 0xFF; }
void play_move_sound() { NR10_REG = 0x00; NR11_REG = 0x81; NR12_REG = 0x43; NR13_REG = 0x73; NR14_REG = 0x86; }
void play_confirm_sound() { NR10_REG = 0x00; NR11_REG = 0x81; NR12_REG = 0x63; NR13_REG = 0xD7; NR14_REG = 0x86; }
void play_win_sound() { unsigned char s; for(s=0; s<3; s++) { NR10_REG = 0x00; NR11_REG = 0x81; NR12_REG = 0x43; NR13_REG = 0x00 + (s*40); NR14_REG = 0x86; delay(100); } }

// --- LOGICA ---
void clear_screen() { for(g_i=0; g_i<20; g_i++) for(g_j=0; g_j<18; g_j++) set_bkg_tile_xy(g_i, g_j, 0); }

void draw_big_piece(unsigned char x, unsigned char y, unsigned char start_tile) {
    set_bkg_tile_xy(x, y, start_tile);       set_bkg_tile_xy(x+1, y, start_tile+1);
    set_bkg_tile_xy(x, y+1, start_tile+2);   set_bkg_tile_xy(x+1, y+1, start_tile+3);
}

void blink_winner(unsigned char tile_start) {
    unsigned char b;
    for(b=0; b<10; b++) {
        for(g_i=0; g_i<3; g_i++) for(g_j=0; g_j<3; g_j++) {
            if(board[g_i][g_j] == winner) {
                if (b % 2 == 0) {
                    set_bkg_tile_xy(t_x[g_i], t_y[g_j], 0);   set_bkg_tile_xy(t_x[g_i]+1, t_y[g_j], 0);
                    set_bkg_tile_xy(t_x[g_i], t_y[g_j]+1, 0); set_bkg_tile_xy(t_x[g_i]+1, t_y[g_j]+1, 0);
                } else { draw_big_piece(t_x[g_i], t_y[g_j], tile_start); }
            }
        }
        delay(100);
    }
}

unsigned char check_win() {
    unsigned char w;
    for(w=0; w<3; w++) {
        if(board[w][0] != 0 && board[w][0] == board[w][1] && board[w][1] == board[w][2]) return board[w][0];
        if(board[0][w] != 0 && board[0][w] == board[1][w] && board[1][w] == board[2][w]) return board[0][w];
    }
    if(board[0][0] != 0 && board[0][0] == board[1][1] && board[1][1] == board[2][2]) return board[0][0];
    if(board[0][2] != 0 && board[0][2] == board[1][1] && board[1][1] == board[2][0]) return board[0][2];
    return 0;
}

void main() {
    const unsigned char txt_tictac[] = {35, 38, 37, 0, 35, 29, 37, 0, 35, 20, 32}; 
    const unsigned char txt_dua[] = {27, 28, 29}; 
    const unsigned char txt_gamer[] = {30, 29, 31, 32, 33};
    const unsigned char txt_aperte[] = {29, 36, 32, 33, 35, 32}; 
    const unsigned char txt_start[] = {34, 35, 29, 33, 35};
    const unsigned char txt_player[] = {36, 39, 29, 40, 32, 33};

    init_sound();
    set_bkg_data(0, 43, game_tiles);
    set_sprite_data(0, 1, cursor_tile);
    set_sprite_tile(0, 0);
    g_last = 0;

    while(1) {
        HIDE_SPRITES; clear_screen();
        for(g_i=0; g_i<11; g_i++) set_bkg_tile_xy(4+g_i, 2, txt_tictac[g_i]);
        for(g_i=0; g_i<3; g_i++) set_bkg_tile_xy(8+g_i, 5, txt_dua[g_i]);
        for(g_i=0; g_i<5; g_i++) set_bkg_tile_xy(7+g_i, 7, txt_gamer[g_i]);
        for(g_i=0; g_i<6; g_i++) set_bkg_tile_xy(7+g_i, 12, txt_aperte[g_i]);
        for(g_i=0; g_i<5; g_i++) set_bkg_tile_xy(7+g_i, 14, txt_start[g_i]);

        SHOW_BKG; DISPLAY_ON;
        while(!(joypad() & J_START)) { wait_vbl_done(); }
        play_confirm_sound(); delay(200);

        for(g_i=0; g_i<3; g_i++) for(g_j=0; g_j<3; g_j++) board[g_i][g_j] = 0;
        cur_x = 0; cur_y = 0; winner = 0; moves = 0;
        turn = g_starter; 
        
        clear_screen();
        for(g_i = 4; g_i < 15; g_i++) {
            set_bkg_tile_xy(g_i, 8, 16);  set_bkg_tile_xy(8, g_i, 17);
            set_bkg_tile_xy(g_i, 11, 16); set_bkg_tile_xy(11, g_i, 17);
        }
        set_bkg_tile_xy(8, 8, 18); set_bkg_tile_xy(11, 8, 18);
        set_bkg_tile_xy(8, 11, 18); set_bkg_tile_xy(11, 11, 18);
        SHOW_SPRITES;

        while(winner == 0 && moves < 9) {
            g_input = joypad();
            if (g_input != g_last) {
                if (g_input & J_UP)    { if(cur_y > 0) cur_y--; play_move_sound(); }
                if (g_input & J_DOWN)  { if(cur_y < 2) cur_y++; play_move_sound(); }
                if (g_input & J_LEFT)  { if(cur_x > 0) cur_x--; play_move_sound(); }
                if (g_input & J_RIGHT) { if(cur_x < 2) cur_x++; play_move_sound(); }
                
                if (g_input & J_A && board[cur_x][cur_y] == 0) {
                    board[cur_x][cur_y] = turn;
                    draw_big_piece(t_x[cur_x], t_y[cur_y], (turn == 1) ? 19 : 23);
                    moves++; play_confirm_sound();
                    winner = check_win();
                    if(winner != 0) {
                         play_win_sound();
                         blink_winner((winner == 1) ? 19 : 23);
                    }
                    if(winner == 0) turn = (turn == 1) ? 2 : 1;
                    delay(200);
                }
            }
            g_last = g_input;
            move_sprite(0, p_x[cur_x] + 12, p_y[cur_y] + 20);
            wait_vbl_done();
        }

        g_starter = (g_starter == 1) ? 2 : 1;
        HIDE_SPRITES;
        if (winner != 0) {
            for(g_i=0; g_i<6; g_i++) set_bkg_tile_xy(6+g_i, 2, txt_player[g_i]);
            set_bkg_tile_xy(13, 2, (winner == 1) ? 41 : 42); 
        } else {
            set_bkg_tile_xy(8, 2, 27); set_bkg_tile_xy(9, 2, 33); set_bkg_tile_xy(10, 2, 29); set_bkg_tile_xy(11, 2, 33);
        }
        while(!(joypad() & J_START)) { wait_vbl_done(); }
        play_confirm_sound(); delay(200); 
    }
}